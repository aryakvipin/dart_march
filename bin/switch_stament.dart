enum size{
  S,M,L,XL,XXL;
}

void main(){
  var value=size.S;
  switch(value){
    case size.S:
      print("small");
      break;
    case size.M:
      print("small");
      break;
    case size.L:
      print("large");
      break;
    case size.XL:
      print("xl");
      break;
    case size.XXL:
      print("xl");
      break;


  }

}